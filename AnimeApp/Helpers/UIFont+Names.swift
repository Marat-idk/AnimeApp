import UIKit

extension UIFont {
    static func ubuntuRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "Ubuntu-Regular", size: size)
    }
    
    static func ubuntuMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Ubuntu-Medium", size: size)
    }
}
